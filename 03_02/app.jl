a = 15
b = 6

println("Operador de Igualdad")
println(a == b)

println("Operador de Desigualdad")
println(a != b)

println("Operador de Mayor que")
println(a > b)

println("Operador de Mayor o igual que")
println(a >= b)

println("Operador de Menor que")
println(a < b)

println("Operador de Menor o igual que")
println(a <= b)